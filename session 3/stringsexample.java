package session3;

public class stringsexample {
	
	public static void main(String[] args) {
		String s1 = "ritika";//string literal
		String s2 = new String("sandhiya");//string object
		char[] c1 = {'j','a','v','a'};
		String s3 = new String(c1);//char to string
		
		System.out.println(s1);
		System.out.println(s2);
		System.out.println(s3);
	}

}
